package ca.team2706.vision.trackerboxreloaded;

public class FrameData {
	
	private int width;
	private int height;
	public FrameData(int width, int height){
		this.width = width;
		this.height = height;
	}
	public int getWidth() {
		return width;
	}
	public int getHeight() {
		return height;
	}
	
}
